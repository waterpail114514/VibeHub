const { contextBridge, ipcRenderer } = require('electron');
contextBridge.exposeInMainWorld('vibehub', {
  minimize: () => ipcRenderer.send('window-minimize'),
  maximize: () => ipcRenderer.send('window-maximize'),
  close: () => ipcRenderer.send('window-hide'),
  quit: () => ipcRenderer.send('window-quit'),
  togglePin: () => ipcRenderer.invoke('window-toggle-pin'),
  getWindowState: () => ipcRenderer.invoke('window-get-state'),
  browseFolder: () => ipcRenderer.invoke('browse-folder'),
  getConfig: () => ipcRenderer.invoke('get-config'),
  saveConfig: (cfg) => ipcRenderer.invoke('save-config', cfg),
  register: (u, p) => ipcRenderer.invoke('auth-register', u, p),
  login: (u, p) => ipcRenderer.invoke('auth-login', u, p),
  getToken: () => ipcRenderer.invoke('auth-get-token'),
  logout: () => ipcRenderer.invoke('auth-logout'),
  checkServer: (serverId) => ipcRenderer.invoke('check-server', serverId),
  listProjects: () => ipcRenderer.invoke('list-projects'),
  createProject: (name) => ipcRenderer.invoke('create-project', name),
  requestDelete: (pid) => ipcRenderer.invoke('request-delete', pid),
  cancelDelete: (pid) => ipcRenderer.invoke('cancel-delete', pid),
  getHistory: (pid) => ipcRenderer.invoke('get-history', pid),
  onUploadProgress: (cb) => { ipcRenderer.removeAllListeners('upload-progress'); ipcRenderer.on('upload-progress', (e, d) => cb(d)); },
  removeUploadProgress: () => ipcRenderer.removeAllListeners('upload-progress'),
  onPullProgress: (cb) => { ipcRenderer.removeAllListeners('pull-progress'); ipcRenderer.on('pull-progress', (e, d) => cb(d)); },
  removePullProgress: () => ipcRenderer.removeAllListeners('pull-progress'),
  pullProject: (pid) => ipcRenderer.invoke('pull-project', pid),
  pushProject: (pid, files) => ipcRenderer.invoke('push-project', pid, files),
  checkLock: (pid) => ipcRenderer.invoke('check-lock', pid),
  getDiff: (pid) => ipcRenderer.invoke('get-diff', pid),
  getPullPreview: (pid) => ipcRenderer.invoke('get-pull-preview', pid),
  aiExplain: (pid, mode) => ipcRenderer.invoke('ai-explain', pid, mode),
  hasClaude: () => ipcRenderer.invoke('has-claude'),
  launchClaude: (pid) => ipcRenderer.invoke('launch-claude', pid),
  getMessages: (pid) => ipcRenderer.invoke('get-messages', pid),
  sendMessage: (pid, content) => ipcRenderer.invoke('send-message', pid, content),
  getChangelogs: (pid) => ipcRenderer.invoke('get-changelogs', pid),
  addChangelog: (pid, content, version, isAi) => ipcRenderer.invoke('add-changelog', pid, content, version, isAi),
  notify: (title, body) => ipcRenderer.invoke('notify', title, body),
  adminStatus: () => ipcRenderer.invoke('admin-status'),
  addAdmin: (username) => ipcRenderer.invoke('admin-add', username),
  removeAdmin: (userId) => ipcRenderer.invoke('admin-remove', userId),
  checkPushPermission: (pid) => ipcRenderer.invoke('check-push-permission', pid),
  requestPush: (pid, summary, fileCount) => ipcRenderer.invoke('request-push', pid, summary, fileCount),
  getPushRequests: () => ipcRenderer.invoke('get-push-requests'),
  reviewPush: (requestId, approved) => ipcRenderer.invoke('review-push', requestId, approved),
  getProjectIcon: (pid) => ipcRenderer.invoke('get-project-icon', pid),
  scanLocal: (pid) => ipcRenderer.invoke('scan-local', pid),
  setProjectPath: (pid, name) => ipcRenderer.invoke('set-project-path', pid, name),
  getProjectInfo: (pid) => ipcRenderer.invoke('get-project-info', pid),
  openProjectFolder: (pid) => ipcRenderer.invoke('open-project-folder', pid),
});
